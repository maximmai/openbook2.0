<h1>About</h1>

<p>
This is an application originally designed and developed for generating and printing invoices. Now we have implemented more features to extend its functionalities, to support not only invoice but also inventory management. <hr/>
Also, we have integrated Google Maps in our system, to make delivery easier for the store manager, by suggesting shortest paths (an approximation implementation of the <a href="http://en.wikipedia.org/wiki/Travelling_salesman_problem">travelling saleman problem</a>). 
</p>

